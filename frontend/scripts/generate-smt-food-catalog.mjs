import fs from 'fs';
import path from 'path';
import dynamicMenu from '../src/data/dynamicMenu.js';

const projectRoot = process.cwd();
const sourceRoot = path.join(projectRoot, 'public', 'SMT FOOD', 'SMT FOOD');
const publicRoot = path.join(projectRoot, 'public', 'assets', 'SMT_FOOD');
const outputCatalog = path.join(projectRoot, 'src', 'data', 'smtFoodCatalog.generated.js');

const IMAGE_EXT_RE = /\.(png|jpe?g|webp)$/i;
const CATEGORY_FOLDER_MAP = {
  'SNACKS': 'snacks',
  'BURGER KABABAB': 'BURGER KABABAB',
  'DINOSAUR': 'DINOSAUR',
  'DESERT': 'desert',
  'COLD DRINKS': 'cold drinks',
  'CAN DRINKS': 'CAN DRINKS',
  'INDIAN FOOD': 'indian food',
  'SUGARCANE': 'SUGARCANE',
  'HOT': 'HOT',
  'SIDES': 'sides',
};

const toSafeCategoryFolder = (folderName) => folderName.replace(/\s+/g, '_');
const toTitleCase = (value) =>
  value
    .toLowerCase()
    .split(' ')
    .filter(Boolean)
    .map((token) => token.charAt(0).toUpperCase() + token.slice(1))
    .join(' ');

const parseFileName = (fileName) => {
  const noExt = fileName.replace(IMAGE_EXT_RE, '');
  const parts = noExt.split('_').filter(Boolean);
  const nameRaw = (parts[0] || 'MenuItem').replace(/[-]+/g, ' ').trim();
  const priceRaw = (parts[1] || '').replace(/[^0-9.]/g, '');
  const parsedPrice = Number.parseFloat(priceRaw);
  return {
    name: toTitleCase(nameRaw),
    price: Number.isFinite(parsedPrice) ? parsedPrice : 0,
  };
};

const ensureDir = (dirPath) => {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
  }
};

const copyImage = (from, to) => {
  ensureDir(path.dirname(to));
  fs.copyFileSync(from, to);
};

const run = () => {
  if (!fs.existsSync(sourceRoot)) {
    throw new Error(`Source not found: ${sourceRoot}`);
  }

  ensureDir(publicRoot);
  const products = [];

  for (const [categoryKey, files] of Object.entries(dynamicMenu)) {
    const category = CATEGORY_FOLDER_MAP[categoryKey] || categoryKey;
    const safeCategory = toSafeCategoryFolder(category);
    const srcCategoryDir = path.join(sourceRoot, category);
    const dstCategoryDir = path.join(publicRoot, safeCategory);
    ensureDir(dstCategoryDir);

    for (const fileName of files || []) {
      if (!IMAGE_EXT_RE.test(fileName)) continue;
      const parsed = parseFileName(fileName);
      const srcPath = path.join(srcCategoryDir, fileName);
      if (!fs.existsSync(srcPath)) continue;
      const dstPath = path.join(dstCategoryDir, fileName);
      copyImage(srcPath, dstPath);

      products.push({
        name: parsed.name,
        price: parsed.price,
        image: `/assets/SMT_FOOD/${safeCategory}/${fileName}`,
        category,
      });
    }
  }

  const contents =
    `// Auto-generated by scripts/generate-smt-food-catalog.mjs\n` +
    `export const smtFoodCatalog = ${JSON.stringify(products, null, 2)};\n` +
    `export default smtFoodCatalog;\n`;

  fs.writeFileSync(outputCatalog, contents, 'utf8');
  console.log(`Generated ${products.length} items -> ${outputCatalog}`);
};

run();
